        //processRequest(request, response);
        Integer numero1 = Integer.parseInt(request.getParameter("txtNumero1"));
        Integer numero2 = Integer.parseInt(request.getParameter("txtNumero2"));
        Integer resultado = numero1 + numero2;
        request.setAttribute("rptasuma", resultado);
        RequestDispatcher midispatcher = request.getRequestDispatcher("/frmResultadoSuma.jsp");
